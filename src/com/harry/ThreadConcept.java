public class ThreadConcept {
    public static void main(String[] args) {
        //Extending Thread
        MyThread1 te1 = new MyThread1();
        MyThread2 te2 = new MyThread2();
        te1.start();
        te2.start();

        // Runnable Interface Method
        MyThreadRunnable1 bullet1 = new MyThreadRunnable1();
        Thread gun1 = new Thread(bullet1);
        MyThreadRunnable2 bullet2 = new MyThreadRunnable2();
        Thread gun2 = new Thread(bullet2);
        gun1.start();
        gun2.start();

        //Constructors from Thread class
        MyThr th1 = new MyThr("Harry");
        MyThr th2 = new MyThr("Ram Candr");
        th1.start();
        th2.start();
        System.out.println("The id of the thread t is " + th1.getId());
        System.out.println("The name of the thread t is " + th1.getName());
        System.out.println("The id of the thread t is " + th2.getId());
        System.out.println("The name of the thread t is " + th2.getName());

        //Java Thread Priorities
        // Ready Queue: T1 T2 T3 T4 T5
        MyThr1 t1 = new MyThr1("Harry1");
        MyThr1 t2 = new MyThr1("Harry2");
        MyThr1 t3 = new MyThr1("Harry3");
        MyThr1 t4 = new MyThr1("Harry4");
        MyThr1 t5 = new MyThr1("Harry5 (most Important)");
        t1.setPriority(Thread.MIN_PRIORITY);
        t2.setPriority(Thread.NORM_PRIORITY);
        t3.setPriority(Thread.NORM_PRIORITY);
        t4.setPriority(Thread.NORM_PRIORITY);
        t5.setPriority(Thread.MAX_PRIORITY);
        t1.start();
        t2.start();
        t3.start();
        t4.start();
        t5.start();

        //Java Thread Methods
        MyNewThr1 teh1 = new MyNewThr1();
        MyNewThr2 teh2 = new MyNewThr2();
        teh1.start();
//        try{
//            t1.join();
//        }
//        catch(Exception e){
//            System.out.println(e);
//        }
        teh2.start();



    }
}
