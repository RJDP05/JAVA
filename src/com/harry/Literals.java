package com.harry;

public class Literals {
    public static void main(String[] args) {
        //pink colour in this code is Keywords
        int x=123;//int is datatype and 123 is a literal
        long y=168431210155498444L;
        float z=4.659f;//we can use F or f in the last (mandatory)
        double d=4.82837;//we can use D or d in the last (not mandatory)
        boolean b=true;
        char s='$';
        String n="RJDP";
        System.out.println("My number are: "+x);
        System.out.println(y);
        System.out.println("My number are: "+z);
        System.out.println(b);
        System.out.println(d);
        System.out.println(s);
        System.out.println(n);
    }
}
